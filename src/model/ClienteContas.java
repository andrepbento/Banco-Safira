package model;

import java.util.HashSet;
import java.util.Set;

public class ClienteContas {
	
	private Cliente cliente;
	private Set<ContaCorrente> contac = new HashSet<ContaCorrente>();
	private Set<ContaPoupanca> contap =  new HashSet<ContaPoupanca>();
	
	public void addContaC(ContaCorrente c){
		contac.add(c);
	}
	
	public void addContaP(ContaPoupanca c){
		contap.add(c);
	}
	
	public Cliente getCliente() {
		return cliente;
	}
	public void setCliente(Cliente cliente) {
		this.cliente = cliente;
	}
	public Set<ContaCorrente> getContac() {
		return contac;
	}
	public void setContac(Set<ContaCorrente> contac) {
		this.contac = contac;
	}
	public Set<ContaPoupanca> getContap() {
		return contap;
	}
	public void setContap(Set<ContaPoupanca> contap) {
		this.contap = contap;
	}
	
	
}
